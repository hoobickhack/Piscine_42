#include <unistd.h>

char	*ft_str(char *str)
{
	int i; 

	i = 0;  
	while (str[i])
	{
		i++; 
	}
	i -= 1; 
	while (i >= 0)
	{
		write(1, &str[i], 1);
		i--; 
	}
	return(str);
}
int main ()
{
	ft_str("ilyes");
}
