#include <unistd.h>

int str_len(char *str) 
{
	int i; 
	int j;

	i = 0;
	j = 0; 
	while (str[i] != '\0')
	{
		if (str[i] == 'z')
			j++; 
		i++; 
	}
	return (j);
}
