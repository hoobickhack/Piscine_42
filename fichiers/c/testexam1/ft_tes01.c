#include <unistd.h>

void	ft_test01(void)

{
   	int a;
	a = '0';
	
	while(a <= '9')
	{
		write(1, &a, 1);
		write(1,"\n", 1);	
		a++;
	}
}

int main()
{ 
	ft_test01();
	return(0);
}

