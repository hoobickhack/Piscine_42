#include <stdio.h>

void	ft_swap(int *a, int	*b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp; 
}
int	main ()
{
	int		a = 14;
	int		b = 15;
	ft_swap(&a,&b);
	printf("a = %d b = %d",a,b);
}
