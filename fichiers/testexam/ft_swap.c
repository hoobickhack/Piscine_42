#include <stdio.h>

void	ft_swap(int	*a, int	*b)
{
	int	temp; 

	temp = *a;
	*a = *b; 
	*b = temp;
}
int	main()
{ 
 int a = 14;
 int b = 16; 
 ft_swap(&a, &b);
 printf("a= %d\n b= %d", a, b);
}
